<h2><i class="fa fa-file-text"></i> Course Overview</h2><div><img height="1px;" src="images/seperator.png"></div>
<p>​It gives us immense pleasure to inform you about our 7th​ edition of - "SolarInstall – Hands-on Solar System design and PV Installation training" <b>with a unique offsite retreat training at a truely Solarized village in the land of forests.</b>
​<br><b>Total duration of the training will be 7 days.</b>
<br>The breakup of 7 days training program is as follows:
​​5 Days theory sessions at Mumbai and 2 Days residential practical training for Hands-On Installation of 4kWp PV plant (<b>On-Grid</b> and <b>Off-Grid</b>)
<br>Residential practical sessions will refreshingly unwind you by <b>camping in tents</b>, enjoying the <b>tribal village crafts</b>, <b>folk dance</b> and <b>scenic beauty</b> around.  The participants will also be given training on marketing & entrepreneurship skill so that they will become the campaigners of solar energy.
<h2><i class="fa fa-check-square"></i> Essentials Required of SolarInstall</h2><div><img height="1px;" src="images/seperator.png"></div>
<h3>1. Participants Selection</h3>
<p>&bull; Engineers, Electricians, Technicians and other solar aspirants</p>
<h3>2. Plant Visit</h3>
<p>&bull; There will a PV system plant visit for the participants included in the workshop</p>
<h3>3. Off Grid and On Grid PV system hands on practice</h3>
<p>&bull; 2 Days residential practical training for Hands-On Installation of 4kWp PV plant (On-Grid and Off-Grid) Residential practical sessions will refreshingly unwind you by camping in tents, enjoying the tribal village crafts, folk dance and scenic beauty around. The participants will also be given training on marketing & entrepreneurship skill so that they will become the campaigners of solar energy. </p>
<h3>4. Certification</h3>
<p>&bull; Certificates will be issued by Skill Council for Green Jobs (SCGJ), Skill India and National Skill Development Corporation (NSDC) under the Ministry of Skill Development and Entrepreneurship (MSDE), Govt. of India through kWatt Solutions Private Limited, SINE, IIT Bombay to the Students/faculties who would attend and successfully complete the training program</p>
<h3>5. Job Opportunities</h3>
<p>&bull; Government has set a target of 100GW solar installation by the year 2022 which will create job opportunities for more than 1.6 million skilled technicians in solar installation</p>
<h2><i class="fa fa-link" ></i> Solar Power Plant Installed Sites and Sites under installation</h2><div><img height="1px;" src="images/seperator.png"></div>
<p>kWatt Solutions is also engaged in design and installation of solar PV systems. It has installed over more than 1500 kW of solar power systems across the country. We use our systems which are already installed or currently being installed for demonstration and on-site practice to our participants. This gives them real life experience of solar PV systems and makes them skillful in installation, repair and maintenance of the solar PV systems. </p>
